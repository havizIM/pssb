<script>
 function load_content(link){

      $.get(`<?= base_url('main/');?>${link}`, function(response){
          $('#content').html(response);
        })
      }

    $(document).ready(function(){
         var link;
         
        if(location.hash){
          link = location.hash.substr(2);
          load_content(link)
        } else {
          location.hash = '#/home'
        }

        $(window).on('hashchange', function(){
          link = location.hash.substr(2);
          load_content(link)
        })
   }) ;

</script>